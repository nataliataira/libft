#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*pt;
	size_t	i;

	pt = (char *)s;
	i = 0;
	while (i < n)
	{
		pt[i++] = c;
	}
	return (s);
}
